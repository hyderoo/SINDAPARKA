<p>Di Sistem Informasi Diagnosa Anak Stunting Parakan Kauman (SINDAPARKA), kami sangat menghargai privasi Anda. Kebijakan privasi ini menjelaskan bagaimana kami mengumpulkan, menyimpan, dan menggunakan informasi pribadi Anda yang kami terima melalui situs web kami.</p>

<div class="font-weight-bold pb-2">Data yang Kami Kumpulkan</div>
<p>
    Kami mengumpulkan informasi pribadi Anda seperti nama, jenis kelamin, alamat email, dan nomor telepon yang Anda berikan saat mendaftar, mengisi formulir, atau menggunakan layanan kami. Selain itu, kami juga mengumpulkan data diagnosa yang Anda lakukan, tetapi hanya Anda yang dapat mengaksesnya.
</p>

<div class="font-weight-bold pb-2">Tujuan Penggunaan Data</div>
<p>
    Kami menggunakan informasi pribadi Anda untuk menyediakan layanan diagnosa stunting, mengirimkan pemberitahuan, atau meningkatkan layanan kami. Kami juga dapat menggunakan informasi Anda untuk menghubungi Anda mengenai produk atau layanan kami yang mungkin menarik bagi Anda.
</p>

<div class="font-weight-bold pb-2">Keamanan Data</div>
<p>
    Kami mengambil langkah-langkah untuk menjaga keamanan data Anda, termasuk enkripsi kata sandi pengguna dan pengaturan hak akses. Namun, tidak ada metode transmisi atau penyimpanan data yang 100% aman. Oleh karena itu, sementara kami berusaha sebaik mungkin untuk melindungi informasi pribadi Anda, kami tidak dapat menjamin keamanan data yang kami terima dari Anda.
</p>

<div class="font-weight-bold pb-2">Pembagian Data</div>
<p>
    Kami tidak akan membagikan informasi pribadi Anda kepada pihak ketiga, kecuali dalam keadaan tertentu seperti:
</p>
<ul>
    <li>Jika diwajibkan oleh hukum atau perintah pengadilan</li>
    <li>Untuk melindungi hak atau properti kami</li>
    <li>Dalam keadaan darurat untuk melindungi keselamatan Anda atau masyarakat lainnya</li>
</ul>

<div class="font-weight-bold pb-2">Akses dan Pembaruan Data</div>
<p>
    Anda dapat mengakses dan memperbarui informasi pribadi Anda melalui halaman profil Anda. Jika Anda memerlukan bantuan untuk memperbarui atau menghapus informasi pribadi Anda, silakan hubungi kami.
</p>

<div class="font-weight-bold pb-2">Perubahan Kebijakan</div>
<p>
    Kebijakan privasi ini dapat berubah dari waktu ke waktu. Perubahan tersebut akan diumumkan di situs web kami dan berlaku segera setelah diumumkan.
</p>

<div class="font-weight-bold pb-2">Pertanyaan dan Umpan Balik</div>
<p>
    Jika Anda memiliki pertanyaan atau umpan balik mengenai kebijakan privasi ini, jangan ragu untuk menghubungi kami.
</p>
